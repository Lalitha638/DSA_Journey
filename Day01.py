#Array example
arr = [10, 20, 30, 40, 50]
print("Array:", arr)

#array Access element
print("First element:", arr[0])
print("Last element:", arr[-1])

#update a value
arr[2] = 99
print("After update:", arr)

#Add an element
arr.append(60)
print("After adding:", arr)

#Remove an element
arr.remove(20)
print("After removing 20:", arr)

#length of an array
print("Length:", len(arr))
